// ---------------------------------------------------------------------------
// PHONETIC TRANSLITERATION MAP (Latin → Arabic)
// ---------------------------------------------------------------------------

/**
 * Maps Latin characters and digraphs to Arabic letter equivalents.
 * Digraphs are listed first for priority matching.
 */
export const LATIN_TO_ARABIC_MAP: Record<string, string> = {
  // Digraphs (must be checked before single characters)
  ch: 'تش',
  sh: 'ش',
  th: 'ث',
  ph: 'ف',
  lh: 'لي',
  nh: 'ني',
  kh: 'خ',
  gh: 'غ',
  dh: 'ذ',
  zh: 'ژ',
  // Vowels
  a: 'ا',
  e: 'ي',
  i: 'ي',
  o: 'و',
  u: 'و',
  // Consonants
  b: 'ب',
  c: 'ك', // default; context-dependent handled in function
  d: 'د',
  f: 'ف',
  g: 'غ',
  h: 'ه',
  j: 'ج',
  k: 'ك',
  l: 'ل',
  m: 'م',
  n: 'ن',
  p: 'ب',
  q: 'ق',
  r: 'ر',
  s: 'س',
  t: 'ت',
  v: 'ف',
  w: 'و',
  x: 'ش',
  y: 'ي',
  z: 'ز',
};

// Digraphs sorted by length descending for greedy matching
const DIGRAPHS = ['ch', 'sh', 'th', 'ph', 'lh', 'nh', 'kh', 'gh', 'dh', 'zh'];

// ---------------------------------------------------------------------------
// TRANSLITERATION FUNCTION
// ---------------------------------------------------------------------------

/**
 * Converts a Latin-script name to Arabic script using phonetic transliteration.
 *
 * Algorithm:
 * 1. Normalize input (trim, lowercase, remove diacritics)
 * 2. Process digraphs first (longer matches take priority)
 * 3. Handle context-sensitive 'c' (before e/i → س, otherwise → ك)
 * 4. Map remaining single characters
 *
 * @param name - The Latin-script name to transliterate
 * @returns Arabic-script approximation of the name
 */
export function transliterateToArabic(name: string): string {
  // Normalize: trim, lowercase, strip diacritics
  const normalized = name
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, ''); // strip combining diacritics (ã→a, é→e, etc.)

  let result = '';
  let i = 0;

  while (i < normalized.length) {
    // Try digraphs first (longest match)
    let matched = false;

    for (const digraph of DIGRAPHS) {
      if (normalized.substring(i, i + digraph.length) === digraph) {
        result += LATIN_TO_ARABIC_MAP[digraph];
        i += digraph.length;
        matched = true;
        break;
      }
    }

    if (matched) continue;

    const char = normalized[i];

    // Context-sensitive 'c': before 'e' or 'i' → س (soft), otherwise → ك (hard)
    if (char === 'c') {
      const next = normalized[i + 1];
      if (next === 'e' || next === 'i') {
        result += 'س';
      } else {
        result += 'ك';
      }
      i++;
      continue;
    }

    // Standard single-character mapping
    const mapped = LATIN_TO_ARABIC_MAP[char];
    if (mapped) {
      result += mapped;
    }
    // Skip unmapped characters (spaces, hyphens, apostrophes, etc.)

    i++;
  }

  return result;
}
